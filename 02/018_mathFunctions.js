var n = 4.593
var n1 = 4.459

// Here 'abs' function gives the absolute value
console.log(Math.abs(n))

// Here 'floor' function removes all digit after the decimal
console.log(Math.floor(n))

// Here 'ceil' function adds 1 to the digit which is at the left of the decimal
console.log(Math.ceil(n))

// Here 'round' function makes the decimal number in round figure digit following the conventional rule of math.
console.log(Math.round(n))
console.log(Math.round(n1))

// Here with 'max' and 'min' function we can found out the maximum and minimum number
console.log(Math.max(200, 300, 400))
console.log(Math.min(200, 300, 400))

// Here 'pow' function works for calculation of number with power
console.log(Math.pow(2, 4))
console.log(Math.pow(5, 2))

// Here 'sqrt' function gives us the square root a number
console.log(Math.sqrt(9))
